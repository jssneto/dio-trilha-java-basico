package Operadores;
public class Operadores2 {
    public static void main(String[] args) {
        
        int numero = 5;

        //x repetição

        numero = numero + 1;

        System.out.println(numero);

        numero++;

        System.out.println(numero);

        System.out.println( ++ numero);

        System.out.println(numero -- );

        System.out.println(numero);

        boolean variavel = true;    

        variavel = !variavel;

        System.out.println(variavel);
    }
    
    

}
